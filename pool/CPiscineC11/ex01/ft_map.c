#include <stdlib.h>

int		*ft_map(int *tab, int length, int (*f)(int))
{
	int		*n;
	int		i;

	i = 0;
	n = malloc(length * sizeof(int));
	while (i < length)
	{
		n[i] = f(tab[i]);
		i++;
	}
	return (n);
}
