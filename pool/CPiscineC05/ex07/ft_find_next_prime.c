int	ft_find_next_prime(int nb)
{
	int i;
	int ok;

	if (nb < 2)
		return (2);
	ok = 0;
	while (ok == 0)
	{
		if (nb > 1 || ok == 0)
		{
			i = nb / 2;
			while (i > 1)
			{
				if (nb % i == 0)
				{
					ok = 0;
					nb++;
				}
				i--;
			}
			ok = 1;
		}
	}
	return (nb);
}
